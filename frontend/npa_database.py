def get_npa_database():
    """Возвращает полный список НПА"""
    return NPA_DATABASE


def find_npa_by_keyword(keyword):
    """Находит НПА по ключевому слову"""
    keyword = keyword.lower()
    results = []
    for npa in NPA_DATABASE:
        if keyword in npa.lower():
            results.append(npa)
    return results


def get_npa_by_number(number):
    """Находит НПА по номеру"""
    results = []
    for npa in NPA_DATABASE:
        if number in npa:
            results.append(npa)
    return results


